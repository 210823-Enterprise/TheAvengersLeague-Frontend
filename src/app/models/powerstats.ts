export class Powerstats {
    
    intelligence: string;
    strength: string;
    speed: string;
    durability: string;
    power: string;
    combat: string;


    constructor(
        intelligence: string,
        strength: string,
        speed: string,
        durability: string,
        power: string,
        combat: string
    ) {
        this.intelligence = intelligence
        this.strength = strength
        this.speed = speed
        this.durability = durability
        this.power = power
        this.combat = combat
    }

}